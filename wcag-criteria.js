export function getCriteriaForDisability(disabilityId) {
  return WCAG_CRITERIA.filter((c) => c.disabilities.includes(disabilityId));
}

export function getCriteriaByLevel(level) {
  return WCAG_CRITERIA.filter((c) => c.level === level);
}

